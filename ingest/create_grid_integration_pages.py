get_dir_make_file_and_recurse('docs/exporting-metrics/connectors')
get_dir_make_file_and_recurse(
    'docs/netdata-cloud/authentication-&-authorization/cloud-authentication-&-authorization-integrations')
get_dir_make_file_and_recurse(
    'docs/logs')
